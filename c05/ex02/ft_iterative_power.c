int		ft_iterative_power(int nb, int power)
{
	int	pow;
	int	i;

	pow = power;
	i = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	while (pow > 1)
	{
		nb *= i;
		pow--;
	}
	return (nb);
}
