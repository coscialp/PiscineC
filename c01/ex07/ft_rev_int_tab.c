void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int tab2[size];

	i = 0;
	j = 0;
	while (i < size)
	{
		tab2[i] = tab[j];
		i++;
		j++;
	}
	j = 0;
	i--;
	while (i >= 0)
	{
		tab[j] = tab2[i];
		j++;
		i--;
	}
}
