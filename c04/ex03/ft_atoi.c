int		ft_atoi(char *str)
{
	int i;
	int nb;
	int nm;

	i = 0;
	nb = 0;
	nm = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			nm++;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		nb *= 10;
		nb += str[i] - '0';
		i++;
	}
	if (nm % 2 == 1)
		return (-nb);
	return (nb);
}
