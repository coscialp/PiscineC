int		ft_is_prime(int nb)
{
	int n;

	n = 3;
	if (nb == 1 || nb <= 0 || (nb % 2 == 0 && nb != 2))
		return (0);
	while (n <= nb / n)
	{
		if (nb % n == 0)
			return (0);
		n = n + 2;
	}
	return (1);
}
